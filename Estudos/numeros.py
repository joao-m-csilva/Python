# Inteiro
numero_inteiro = 4
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real =", numero_real)


#type()
print("Tipo da variável do inteiro =", type(numero_inteiro))
print("Tipo da variável do real =", type(numero_real))

#soma
soma = 1 + 1
print("Soma =", soma)   

#subtracao

subtracao = 5 - 2
print("Subtração =", subtracao)

#multiplicacao

multiplicacao = 3 * 4
print("Multiplicação =", multiplicacao)

#divisao = sempre que usada, sempre terei um resultado float

divisao = 4 / 2
print("Divisão =", divisao)
print("Tipo da variável =", type(divisao))

#divisao em inteiro 

divisao2 = 5 // 2
print("Divisão em inteiro =", divisao2)
print("Tipo da variável =", type(divisao))

# conversao em inteiro
print("Valor da divisão (int) =", int(divisao))

#conversao em float
print("Valor da divisão (float) =", float(multiplicacao))


#Módulo %

modulo = 5 % 2
print("Módulo =", modulo)
print("Tipo da variável =", type(modulo))   
